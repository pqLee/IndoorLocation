package com.intel.indoorlocation.DataManager;

public class SampleData {

//    public int mId;
    public String mTag;
    public float mX;
    public float mY;
    public float mZ;


    public SampleData(){

    }

    public String getmTag(){
        return mTag;
    }
    public void setmTag(String tag){
        this.mTag = tag;
    }


    public float getmX(){
        return mX;
    }
    public void setmX(float x){
        this.mX = x;
    }
    public float getmY(){
        return mY;
    }
    public void setmY(float y){
        this.mY = y;
    }

    public float getmZ(){
        return mZ;
    }
    public void setmZ(float z){
        this.mZ = z;
    }
}
